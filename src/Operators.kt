fun main() {
    var i: Int = 10
    var j: Int = 15

    // Arithmetic Operators
    println(i + j)
    println(i - j)
    println(i * j)
    println(i.toFloat() / j)

    // Relational Operators
    println(i > j)
    println(i < j)
    println(i >= j)
    println(i <= j)
    println(i == j)
    println(i != j)

    // Increment / Decrement Operators
//    i++
    println(i)
    --j
    println(j)

    println(i++ + ++i)
}